async def upsert_gist(db: AsyncSession, app_id: str, generated_by: str, content: str) -> int:
    gist_id = await scalar(
        db,
        "SELECT id FROM gists WHERE application_id = :application_id",
        {"application_id": app_id},
    )

    template_id = await scalar(
        db,
        "SELECT id FROM gist_templates WHERE is_active = true ORDER BY id LIMIT 1",
    )

    if gist_id:
        await db.execute(
            text(
                """
                UPDATE gists
                SET template_id = :template_id,
                    content = :content,
                    generated_by = :generated_by,
                    generated_at = :generated_at,
                    last_modified_by = :last_modified_by,
                    last_modified_at = :last_modified_at
                WHERE id = :id
                """
            ),
            {
                "id": gist_id,
                "template_id": template_id,
                "content": content,
                "generated_by": generated_by,
                "generated_at": utc_now() - timedelta(days=1),
                "last_modified_by": generated_by,
                "last_modified_at": utc_now() - timedelta(hours=4),
            },
        )
        return int(gist_id)

    inserted = await db.execute(
        text(
            """
            INSERT INTO gists
                (application_id, template_id, content, generated_by, generated_at, last_modified_by, last_modified_at)
            VALUES
                (:application_id, :template_id, :content, :generated_by, :generated_at, :last_modified_by, :last_modified_at)
            RETURNING id
            """
        ),
        {
            "application_id": app_id,
            "template_id": template_id,
            "content": content,
            "generated_by": generated_by,
            "generated_at": utc_now() - timedelta(days=1),
            "last_modified_by": generated_by,
            "last_modified_at": utc_now() - timedelta(hours=4),
        },
    )
    return int(inserted.scalar_one())


async def upsert_mom(
    db: AsyncSession,
    app_id: str,
    gist_id: int,
    created_by: str,
    finalized_by: str | None,
    status: str,
) -> None:
    mom_id = await scalar(db, "SELECT id FROM moms WHERE application_id = :application_id", {"application_id": app_id})

    content = (
        "MINUTES OF MEETING\n\n"
        f"Application ID: {app_id}\n"
        "Committee observed cumulative impact, mitigation commitments, and compliance timeline.\n"
        "Decision: Subject to conditions in annexure, proposal may proceed to next compliance stage.\n"
    )

    payload = {
        "application_id": app_id,
        "gist_id": gist_id,
        "content": content,
        "status": status,
        "integrity_hash": f"demo-{app_id[:8]}-{status.lower()}",
        "created_by": created_by,
        "created_at": utc_now() - timedelta(hours=18),
        "finalized_by": finalized_by,
        "finalized_at": utc_now() - timedelta(hours=2) if status == "FINALIZED" else None,
        "mom_docx_path": f"/generated/mom_{app_id}.docx",
        "mom_pdf_path": f"/generated/mom_{app_id}.pdf",
        "gist_docx_path": f"/generated/gist_{app_id}.docx",
        "gist_pdf_path": f"/generated/gist_{app_id}.pdf",
    }

    if mom_id:
        await db.execute(
            text(
                """
                UPDATE moms
                SET gist_id = :gist_id,
                    content = :content,
                    status = :status,
                    integrity_hash = :integrity_hash,
                    created_by = :created_by,
                    finalized_by = :finalized_by,
                    finalized_at = :finalized_at,
                    mom_docx_path = :mom_docx_path,
                    mom_pdf_path = :mom_pdf_path,
                    gist_docx_path = :gist_docx_path,
                    gist_pdf_path = :gist_pdf_path
                WHERE id = :id
                """
            ),
            {**payload, "id": mom_id},
        )
    else:
        await db.execute(
            text(
                """
                INSERT INTO moms
                    (application_id, gist_id, content, status, integrity_hash, created_by, created_at,
                     finalized_by, finalized_at, mom_docx_path, mom_pdf_path, gist_docx_path, gist_pdf_path)
                VALUES
                    (:application_id, :gist_id, :content, :status, :integrity_hash, :created_by, :created_at,
                     :finalized_by, :finalized_at, :mom_docx_path, :mom_pdf_path, :gist_docx_path, :gist_pdf_path)
                """
            ),
            payload,
        )


async def append_audit_snapshot(db: AsyncSession, actor_id: str, actor_role: str, app_id: str, status: str) -> None:
    exists = await scalar(
        db,
        "SELECT 1 FROM audit_logs WHERE action = :action AND entity_id = :entity_id",
        {"action": f"DEMO_{status}", "entity_id": app_id},
    )
    if exists:
        return

    await db.execute(
        text(
            """
            INSERT INTO audit_logs
                (actor_id, actor_role, action, description, entity_type, entity_id, ip_address)
            VALUES
                (:actor_id, :actor_role, :action, :description, 'APPLICATION', :entity_id, CAST(:ip_address AS inet))
            """
        ),
        {
            "actor_id": actor_id,
            "actor_role": actor_role,
            "action": f"DEMO_{status}",
            "description": f"Demo data seeded for application in {status} state.",
            "entity_id": app_id,
            "ip_address": "127.0.0.1",
        },
    )


