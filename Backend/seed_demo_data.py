async def ensure_sectors_and_parameters(db: AsyncSession) -> tuple[dict[str, int], dict[str, dict[str, int]]]:
    sector_ids: dict[str, int] = {}
    param_ids_by_sector: dict[str, dict[str, int]] = {}

    for name, description in SECTORS:
        sector_id = await scalar(db, "SELECT id FROM sectors WHERE name = :name", {"name": name})
        if not sector_id:
            inserted = await db.execute(
                text("INSERT INTO sectors (name, description) VALUES (:name, :description) RETURNING id"),
                {"name": name, "description": description},
            )
            sector_id = inserted.scalar_one()
        else:
            await db.execute(
                text("UPDATE sectors SET description = :description WHERE id = :id"),
                {"id": sector_id, "description": description},
            )
        sector_ids[name] = int(sector_id)

        param_ids_by_sector[name] = {}
        for p_name, p_key, p_type, is_required, display_order in SECTOR_PARAMS[name]:
            param_id = await scalar(
                db,
                "SELECT id FROM sector_parameters WHERE sector_id = :sector_id AND key = :key",
                {"sector_id": sector_id, "key": p_key},
            )
            if not param_id:
                inserted = await db.execute(
                    text(
                        """
                        INSERT INTO sector_parameters
                            (sector_id, name, key, type, is_required, display_order)
                        VALUES
                            (:sector_id, :name, :key, :type, :is_required, :display_order)
                        RETURNING id
                        """
                    ),
                    {
                        "sector_id": sector_id,
                        "name": p_name,
                        "key": p_key,
                        "type": p_type,
                        "is_required": is_required,
                        "display_order": display_order,
                    },
                )
                param_id = inserted.scalar_one()
            else:
                await db.execute(
                    text(
                        """
                        UPDATE sector_parameters
                        SET name = :name,
                            type = :type,
                            is_required = :is_required,
                            display_order = :display_order
                        WHERE id = :id
                        """
                    ),
                    {
                        "id": param_id,
                        "name": p_name,
                        "type": p_type,
                        "is_required": is_required,
                        "display_order": display_order,
                    },
                )

            param_ids_by_sector[name][p_key] = int(param_id)

    return sector_ids, param_ids_by_sector


