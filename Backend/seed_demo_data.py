def utc_now() -> datetime:
    return datetime.now(timezone.utc)


async def scalar(db: AsyncSession, sql: str, params: dict[str, Any] | None = None) -> Any:
    result = await db.execute(text(sql), params or {})
    return result.scalar()


async def ensure_roles(db: AsyncSession) -> dict[str, int]:
    role_labels = {
        "ADMIN": "Administrator",
        "PP": "Project Proponent",
        "SCRUTINY": "Scrutiny Officer",
        "MOM": "Minutes of Meeting",
    }

    role_ids: dict[str, int] = {}
    for role_name, role_label in role_labels.items():
        role_id = await scalar(db, "SELECT id FROM roles WHERE name = :name", {"name": role_name})
        if not role_id:
            inserted = await db.execute(
                text(
                    "INSERT INTO roles (name, label) VALUES (:name, :label) RETURNING id"
                ),
                {"name": role_name, "label": role_label},
            )
            role_id = inserted.scalar_one()
        elif role_label:
            await db.execute(
                text("UPDATE roles SET label = :label WHERE id = :id"),
                {"id": role_id, "label": role_label},
            )
        role_ids[role_name] = int(role_id)

    return role_ids


