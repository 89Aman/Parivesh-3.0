async def replace_application_parameters(
    db: AsyncSession,
    app_id: str,
    param_ids: dict[str, int],
    values: dict[str, Any],
) -> None:
    await db.execute(
        text("DELETE FROM application_parameters WHERE application_id = :app_id"),
        {"app_id": app_id},
    )

    for key, param_id in param_ids.items():
        if key not in values:
            continue

        value = values[key]
        value_text = None
        value_number = None
        value_boolean = None

        if isinstance(value, bool):
            value_boolean = value
        elif isinstance(value, (int, float, Decimal)):
            value_number = Decimal(str(value))
        else:
            value_text = str(value)

        await db.execute(
            text(
                """
                INSERT INTO application_parameters
                    (application_id, sector_parameter_id, value_text, value_number, value_boolean)
                VALUES
                    (:application_id, :sector_parameter_id, :value_text, :value_number, :value_boolean)
                """
            ),
            {
                "application_id": app_id,
                "sector_parameter_id": param_id,
                "value_text": value_text,
                "value_number": value_number,
                "value_boolean": value_boolean,
            },
        )


