def get_ordered_undertaking_points() -> list[dict]:
    """Return points with auto-assigned display_order per mineral_type."""
    type_counters: dict[str, int] = {}
    ordered: list[dict] = []
    for point in PP_UNDERTAKING_POINTS:
        mt = point["mineral_type"]
        idx = type_counters.get(mt, 0) + 1
        type_counters[mt] = idx
        ordered.append({**point, "display_order": idx})
    return ordered
